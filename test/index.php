<?php
namespace App;

interface Gun
{
	public function shoot();
}


class Ak47 implements Gun
{
	public function shoot()
	{
		echo "Chop chop chop chop <br />";
	}
}

class M16 implements Gun
{
	public function shoot()
	{
		echo "Dut dut dut dut <br />";
	}
}

class Glock implements Gun
{
	public function shoot()
	{
		echo "Bang bang <br />";
	}
}

class DesertEagle implements Gun
{
	public function shoot()
	{
		echo "Boooom boooom <br />";
	}
}

class Attack
{

	public function __construct(Gun $gun, Gun $gun2)
	{
		foreach([$gun, $gun2] as $attacker)
		{
			$attacker->shoot();
		}
	}
}

$fight1 = new Attack(new DesertEagle(), new Ak47());
$fight2 = new Attack(new Glock(), new M16());


